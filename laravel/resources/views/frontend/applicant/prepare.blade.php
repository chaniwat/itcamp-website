    <li>สิ่งที่เกี่ยวข้องกับการพนันทุกชนิด</li>
    <li>เครื่องดื่มแอลกอฮอล์และสิ่งมึนเมาทุกชนิด เช่น สุรา เบียร์</li>
    <li>อุปกรณ์ที่สามารถใช้เป็นอาวุธได้ เช่น มีด</li>
