<h3 style="margin-bottom: 1rem;"><b>เตรียมความพร้อมก่อนผจญภัย !!!</b></h3>

<h5>ในการออกผจญภัยนั้น ก่อนอื่นเลยน้องๆ จะต้องเตรียมของให้ครบดังนี้</h5>
<ul>
    <li>ชุดนักเรียน 2 ชุด (สำหรับใส่มาวันแรกและใส่กลับวันสุดท้าย)</li>
    <li>ชุดลำลอง 4 ชุด (เป็นชุดสุภาพ กางเกงขายาวเลยเข่า)</li>
    <li>ชุดนอน 4 ชุด</li>
    <li>เสื้อกันหนาว (ห้องเรียนเป็นห้องแอร์นะครับ)</li>
    <li>ไม้แขวนเสื้อ</li>
    <li>หมอน (พี่ๆ มีถุงนอนแจกจ้า)</li>
    <li>อุปกรณ์อาบน้ำ เช่น สบู่, ยาสระผม, แปรงสีฟัน, ยาสีฟัน ฯลฯ</li>
    <li>ผ้าถุง (สำหรับน้องผู้หญิง)</li>
    <li>ของใช้ส่วนตัว</li>
    <li>รองเท้าแตะ</li>
    <li>รองเท้าผ้าใบ</li>
    <li>ยารักษาโรคประจำตัว (ถ้ามี)</li>
    <li>เครื่องเขียน</li>
    <li>และแน่นอน สิ่งที่ขาดไม่ได้นั่นก็คือ.. <b>ชุดและเครื่องประดับสำหรับกิจกรรม Virus 4.0 !!</b></li>
</ul>
<b>ยินดีด้วย!!</b> น้องได้รับบัตรเชิญเข้าร่วมกิจกรรมภายในค่าย #ITCAMP13 กิจกรรมสุดหฤหรรษ์ที่จะพาน้องๆ เข้าสู่ประตูแห่งช่วงเวลาของค่ำคืนมหัศจรรย์...ตะลุย "พิพิธภัณฑ์ Virus 4.0" พิพิธภัณฑ์ไฮเทคสุดพิลึกที่ไม่เคยปรากฏที่ไหนมาก่อน จะเกิดอะไรขึ้นในค่าย ITCAMP13 ต้องมาพบกัน!!<span class="break"></span>
<b>*คำเตือน - อย่าลืมเตรียมเครื่องแต่งกาย หรือเครื่องประดับที่สะท้อนแสงในที่มืดมาด้วยล่ะ [ถ้าลืมเอามา อย่าหาว่าไม่เตือนก็แล้วกัน!]</b><span class="break"></span>

น้องๆ ไม่จำเป็นต้องนำสิ่งของมีค่ามาค่าย เช่น คอมพิวเตอร์โน็ตบุ๊ค <b><u>หากเกิดการสูญหาย ทางค่ายจะไม่รับผิดชอบไม่ว่ากรณีใดๆ ทั้งสิ้น</u></b> <span class="break"></span>

<h5>นอกจากสิ่งที่ต้องนำมาแล้ว ยังมีสิ่งที่ <b><u>ห้าม</u></b> นำมาอีกด้วย</h5>
<ul>
