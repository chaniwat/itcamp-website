<h3 style="margin-bottom: 1rem;"><b>รายละเอียดการยืนยันสิทธิ์</b></h3>

น้้องๆ จะต้องยืนยันสิทธิ์โดยการแนบเอกสารดังนี้
<ol style="margin-bottom: 0.25rem;">
    <li>หลักฐานการโอนเงิน</li>
</ol>
น้องๆ สามารถส่งหลักฐานได้ในหน้า <a data-toggle="tab" href="#evidence" role="tab"><b>ส่งหลักฐาน</b></a> โดยให้น้องๆ แนบไฟล์รูปภาพที่เป็นสลิปหรือหลักฐานการโอนเงิน<span class="break"></span>

<b>หมายเหตุ</b>
<ul>
    <li>หลักฐานดังกล่าวให้น้องๆถ่ายรูปหรือแสกนมาก็ได้ แต่ขอให้ภาพชัดเจนนะครับ</li>
    <li>หลักฐานการโอนเงิน เช่น สำเนาการโอน, สลิป เป็นต้น</li>
    <li>หากไม่ส่งเอกสาร จะถือว่าสละสิทธิ์การเข้าร่วมค่าย ITCAMP ครั้งที่ 13</li>
    <li>สามารถโอนเงินและแนบเอกสารได้ถึงวันที่ 22 พฤษภาคม 2560 อย่ารอช้าน้า พี่รออยู่ ^^</li>
</ul>

